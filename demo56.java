public class demo56 {
    public static void main(String[] args) {
        for (int num = 1; num <=9; num++) {
            for (int sde = 1; sde <= num; sde++) {
                System.out.print(num+"*"+sde+"="+num*sde+"\t");
            }
            System.out.println();
        }
    }
}
